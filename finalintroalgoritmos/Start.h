void instructions(int transition[FILAS][COLUMNAS]) {
	for (int i = 0; i < FILAS; i++)
	{
		for (int j = 0; j < COLUMNAS; j++)
		{
			setxy(j, i);
			if (transition[i][j] == 0) changeColor(15);
			if (transition[i][j] == 1) changeColor(1);
			if (transition[i][j] == 2) changeColor(5);


			cout << (char)219;
		}
	}
	setxy(20, 4);
	cout << "INSTRUCCIONES: " << endl;
	setxy(10, 6);
	cout << "- Presiona la X lo más rápido que puedas." << endl;
	setxy(10, 8);
	cout << "- La cantidad de X que necesitas presionar será mayor cada ronda. " << endl;
	setxy(10, 10);
	cout << "- En caso no pases una ronda, perderas. " << endl;
	system("pause>0");

}

void drawInauguration(int start[FILAS][COLUMNAS]) {
	for (int i = 0; i < FILAS; i++)
	{
		for (int j = 0; j < COLUMNAS; j++)
		{
			setxy(j, i);
			if (start[i][j] == 0) changeColor(0); //fondo
			if (start[i][j] == 1) changeColor(2);// primer aro
			if (start[i][j] == 2) changeColor(15);// segundo aro
			if (start[i][j] == 3) changeColor(3);//tercer aro
			if (start[i][j] == 4) changeColor(1);// cuarto aro 
			if (start[i][j] == 5) changeColor(4); //quinto aro
			if (start[i][j] == 6) changeColor(13); //letras de abajo
			cout << (char)219;
		}
	}
	system("pause>0");
	instructions(transition);
}

